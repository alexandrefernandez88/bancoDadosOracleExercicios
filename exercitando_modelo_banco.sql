

CREATE TABLE MAT_CATEGORIAS (
  CAT_COD NUMBER(3),
  CAT_PAI NUMBER(3),
  CAT_NOME VARCHAR2(30 BYTE)
);




CREATE TABLE MAT_CIDADES (
  CID_COD NUMBER(6),
  CID_NOME VARCHAR2(30 BYTE),
  EST_UF VARCHAR2(2 BYTE)
);




CREATE TABLE MAT_CLIENTES (
  CLI_COD NUMBER(6),
  CID_COD NUMBER(6),
  CLI_NOME VARCHAR2(50 BYTE),
  CLI_CPF VARCHAR2(11 BYTE),
  CLI_SEXO CHAR(1 BYTE),
  CLI_DTNASC DATE
);




CREATE TABLE MAT_ESTADOS (
  EST_UF VARCHAR2(2 BYTE),
  EST_NOME VARCHAR2(20 BYTE)
);





CREATE TABLE MAT_FORNECEDORES (
  FOR_COD NUMBER(4),
  FOR_NOME VARCHAR2(40 BYTE),
  FOR_FONE CHAR(15 BYTE),
  FOR_CONTATO VARCHAR2(30 BYTE)
);





CREATE TABLE MAT_ITENSNOTAS (
  NOT_NUM NUMBER(6),
  PRO_COD NUMBER(6),
  IT_QUANT NUMBER(6),
  IT_PRVENDA NUMBER(12)
);


CREATE TABLE MAT_NOTAS (
  NOT_NUM NUMBER(6),
  CLI_COD NUMBER(6),
  NOT_DATA DATE,
  NOT_TOTAL NUMBER(12,2)
);







CREATE TABLE MAT_PRODS_FORNECS (
  PRO_COD NUMBER(6),
  FOR_COD NUMBER(4)
);



CREATE TABLE MAT_PRODUTOS (
  PRO_COD NUMBER(6),
  PRO_DESC VARCHAR2(50 BYTE),
  PRO_ESTOQUE NUMBER(6),
  PRO_PRCUSTO NUMBER(12,2),
  PRO_PRVENDA NUMBER(12,2),
  CAT_COD NUMBER(3),
  PRO_ESTMIN NUMBER(6),
  PRO_ESTMAX NUMBER(6)
);





